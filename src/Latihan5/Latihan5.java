    private void ProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProsesActionPerformed
        // TODO add your handling code here
        float Diskon, Reguler=11500, Total=0, berat, aturdiskon;
        String nama,notelpon, jenisjs;
        
        
        nama = txtNama.getText();
        notelpon = txtTelpon.getText();
        berat = Float.parseFloat(txtBerat.getText());
        Diskon = Float.valueOf(slider.getValue());        
                
        if (js1.isSelected()){
            jenisjs = js1.getText();
            aturdiskon = Reguler * (Diskon/100)* berat;
            Total = (Reguler * berat)- aturdiskon;
           //F.Total(Total);
        }
        else if (JS2.isSelected()){
            jenisjs = JS2.getText();
            aturdiskon = (float) (Reguler * 1.5 * (Diskon/100)* berat);
            Total = (float)(Reguler * berat * 1.5)- aturdiskon;
        }
        else {
            JOptionPane.showMessageDialog(null, "Pilih Jenis Jasa Terlebih Dahulu","WARNING", JOptionPane.INFORMATION_MESSAGE);
                return;
        }
        txtTotal.setText(""+Total);
        Struk.setText("************STRUK PEMBAYARAN************\n"
                + " Nama\t : "+nama+
                " \n No. Telpon\t : "+notelpon+
                " \n Berat Barang\t : "+berat+
                " \n Diskon\t : "+Total+
                " \n Jenis Jasa\t : "+jenisjs+
                " \n Total Harga\t : Rp."+Total);
    }//GEN-LAST:event_ProsesActionPerformed

